    <h2>Why Flexbox? - A brief and essential guide for beginners</h2>
       <p>If you think the “float” element is the best to use in floating things around, that you may be wrong. There is one more element, more holistic, more useful and more practical to apply on the whole page of layout. This element called “Flexbox”. But, why flexbox? Here are there major reasons that web designers including beginners) should use it. According to “Basic concept of flexbox” from MDN web docs, “The Flexible Box Module, usually referred to as flexbox, was designed as a one-dimensional layout model, and as a method that could offer space distribution between items in an interface and powerful alignment capabilities.” There are three crucial points of using flexbox: The great strength of scaling elements and wide-acceptable browser support, Simple way to get starting with website layout and smart use as a fallback technique for grid.
     
       <p>Are you tired of panning out a layout without any technique? Here’s what the variable “flexbox” does: It can be used for scaling elements. Written by Sitepoint in “A friendly introduction to flexbox for beginners”, the article mentioned that “page content can be laid out in any direction (to the left, to the right, downwards or even upwards)”; also, “items can “flex” their sizes to respond to the available space and can be aligned with respect to their container or each other.” The flexbox is able to rearrange the elements on the same webpage easily and smoothly. Moreover, it provides the way that designers can achieve equal column layouts with “flexbox” functionality. Not to mention that flexbox can be applied into most of major browsers in the markets. For examples, on the report of “6 Reasons to Start Using Flexbox” by bitsofcode, “The main reason I hadn’t been using flexbox was because of a perceived lack of browser support. But in actual fact, flexbox is very well supported, at 95.89% global support. If you disregard IE 10 and below, which Microsoft says you now can, this number is even higher; Flexbox has even more support than other features I still use such as 2D transforms (with 91.85%) or even position: fixed (with 92.98%).”</p>
 
     <p>Another reason that it’s necessary to use flexbox is because it’s easy to get used to.
     In the article “6 Reasons to Start Using Flexbox” written by bitsofcode, the author said that basic syntax included in the code always contain “display: flex;”; furthermore,  in another article “A Complete Guide to Flexbox” composed by CSS-TRICKS, all you need to do is know what’s your need in variable “flex-direction”, “justify-content” and “ align-content”. Also, “Since flexbox is a whole module and not a single property, it involves a lot of things including its whole set of properties. Some of them are meant to be set on the container (parent element, known as "flex container") whereas the others are meant to be set on the children (said "flex items").”  This means that designers have the “full” control that they want to arrange or rearrange with the layout and contents of elements; including defining complex web pages with flexbox is all about nesting boxes.</p>
    
     <p>The other point is that, if designers are not familiar with CSS grid, flexbox can be an equal better use of page layouts. The basic difference between CSS Grid Layout and CSS Flexbox Layout is that flexbox was designed for layout in one dimension - either a row or a column. However, according to “As you lay out your items you want to control the dimensions of the items in that one dimension or control the spacing between items. These are the uses that flexbox was designed for.” Other than this, according to “CSS Grid Layout with Flexbox Fallback” by Kirsten Swanson, “Since Flexbox has more support than CSS Grid in most of the older browser versions, we can apply a Flexbox layout as a fallback if CSS Grid isn’t supported.”(Medium.com) Therefore, we can tell that designers are able to combine the functionality of CSS grid and flexbox together.</p>
    
     <p>In brief, flexbox gave us a ton of amazing new tools for laying out a web page. Compare these techniques to what we were able to do with floats, and CSS grid which is more limited due to compatible browsers. In many ways, flexbox has certain advantages to use and to be applied on many website for better user experience. As you can see, flexbox can make designer’s work so much easier if they are required to control the position of elements on a website. It’s rock solid and makes any hacks, collapsing containers or other weird stuff they have had to deal with every day, obsolete. Besides, flexbox offers an easy and straightforward way to code and more easily to get accessed. But the most important thing is, think about the best way to present the content that you have and then see how flexbox or other layout methods can help you achieve it.</p>

